class Employee {
    constructor(name, age, mobile) {
        this.name = name;
        this.age = age;
        this.mobile = mobile;
    }
    getName() {
        return this.name;
    }
    setName(name) {
        this.name = name;
    }
    getAge() {
        return this.age;
    }
    setAge(age) {
        this.age = age;
    }
    getMobile(mobile) {
        this.mobile = mobile;
    }
}
